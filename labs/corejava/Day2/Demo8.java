package Day2;

public class Demo8 {
    public static void main(String[] args) {
        int number=8;

        switch (number)
        {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("Not in the list");
        }
    }
}
